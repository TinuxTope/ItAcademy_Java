package TascaS1_04.N1.n1exercici1CristinaTomas;

public class Mes {
    private String nom;
    private int posicio;

    public Mes(String nom, int posicio) {
        this.nom = nom;
        this.posicio = posicio;
    }

    public String getNom() {
        return nom;
    }

    public int getPosicio() {
        return posicio;
    }

}